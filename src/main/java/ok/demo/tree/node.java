package ok.demo.tree;

public class node {

    private int value;
    private node left;
    private node right;
    
    public int getValue() {
        return value;
    }
    public void setValue(int value) {
        this.value = value;
    }
    public node getLeft() {
        return left;
    }
    public void setLeft(node left) {
        this.left = left;
    }
    public node getRight() {
        return right;
    }
    public void setRight(node right) {
        this.right = right;
    }
    
    
}
